// Задача №4
// Функція ділення. Не має повертати NaN, Infinity або кидати помилку.
// Повертає рядок виду: "Результат ділення: 5"

function divide(a, b) {
  // якщо аргументи не передані
  if (a === undefined || b === undefined) {
    return "Помилка: не передані аргументи";
  }

  // перевірка на некоректні дані
  if (Number.isNaN(a) || Number.isNaN(b)) {
    return "Помилка: передані некоректні дані";
  }

  // перевірка на ділення на нуль
  if (b === 0) {
    return "Помилка: ділити на нуль не можна!";
  }

  const result = a / b;
  return `Результат ділення: ${result}`;
}

